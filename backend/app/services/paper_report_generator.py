def markdown_to_html(md_text: str) -> str:
    """Convert markdown analysis text to HTML with tables and math support."""
    if not md_text:
        return ""

    import markdown

    # Protect LaTeX math from markdown processing
    # Block math: $$...$$ → placeholder
    math_blocks: list[str] = []
    def _save_block_math(m: re.Match) -> str:
        math_blocks.append(m.group(1))
        return f"MATHBLOCK_{len(math_blocks) - 1}_ENDMATH"

    text = re.sub(r'\$\$(.+?)\$\$', _save_block_math, md_text, flags=re.DOTALL)

    # Inline math: $...$ → placeholder (but not $$)
    math_inlines: list[str] = []
    def _save_inline_math(m: re.Match) -> str:
        math_inlines.append(m.group(1))
        return f"MATHINLINE_{len(math_inlines) - 1}_ENDMATH"

    text = re.sub(r'(?<!\$)\$([^\$\n]+?)\$(?!\$)', _save_inline_math, text)

    # Convert markdown to HTML using python-markdown with extensions
    html = markdown.markdown(
        text,
        extensions=["tables", "fenced_code", "nl2br"],
    )

    # Restore block math (keep LaTeX for MathJax rendering)
    for i, formula in enumerate(math_blocks):
        html = html.replace(
            f"MATHBLOCK_{i}_ENDMATH",
            f'<div class="math-block">$${formula}$$</div>'
        )

    # Restore inline math (keep LaTeX for MathJax rendering)
    for i, formula in enumerate(math_inlines):
        html = html.replace(
            f"MATHINLINE_{i}_ENDMATH",
            f'<span class="math-inline">${formula}$</span>'
        )

    return html


def _latex_to_html_math(formula: str, block: bool = False) -> str:
    """Convert LaTeX math to readable HTML without requiring JavaScript."""
    f = formula.strip()

    # Common LaTeX → HTML replacements
    replacements = [
        (r'\\frac\{([^}]+)\}\{([^}]+)\}', r'(\1)/(\2)'),
        (r'\\sum_\{([^}]+)\}\^\{([^}]+)\}', r'Σ<sub>\1</sub><sup>\2</sup>'),
        (r'\\sum', 'Σ'),
        (r'\\prod', 'Π'),
        (r'\\int', '∫'),
        (r'\\infty', '∞'),
        (r'\\alpha', 'α'), (r'\\beta', 'β'), (r'\\gamma', 'γ'),
        (r'\\delta', 'δ'), (r'\\epsilon', 'ε'), (r'\\theta', 'θ'),
        (r'\\lambda', 'λ'), (r'\\mu', 'μ'), (r'\\sigma', 'σ'),
        (r'\\pi', 'π'), (r'\\phi', 'φ'), (r'\\omega', 'ω'),
        (r'\\Delta', 'Δ'), (r'\\Sigma', 'Σ'), (r'\\Omega', 'Ω'),
        (r'\\nabla', '∇'), (r'\\partial', '∂'),
        (r'\\leq', '≤'), (r'\\geq', '≥'), (r'\\neq', '≠'),
        (r'\\approx', '≈'), (r'\\times', '×'), (r'\\cdot', '·'),
        (r'\\in', '∈'), (r'\\notin', '∉'), (r'\\subset', '⊂'),
        (r'\\rightarrow', '→'), (r'\\leftarrow', '←'), (r'\\Rightarrow', '⇒'),
        (r'\\forall', '∀'), (r'\\exists', '∃'),
        (r'\\sqrt\{([^}]+)\}', r'√(\1)'),
        (r'\\mathbb\{([^}]+)\}', r'\1'),
        (r'\\mathcal\{([^}]+)\}', r'\1'),
        (r'\\mathrm\{([^}]+)\}', r'\1'),
        (r'\\text\{([^}]+)\}', r'\1'),
        (r'\\textbf\{([^}]+)\}', r'<strong>\1</strong>'),
        (r'\\ell', 'ℓ'),
        (r'\^(\w)', r'<sup>\1</sup>'),
        (r'\^\{([^}]+)\}', r'<sup>\1</sup>'),
        (r'_(\w)', r'<sub>\1</sub>'),
        (r'_\{([^}]+)\}', r'<sub>\1</sub>'),
        (r'\\,', ' '),
        (r'\\;', ' '),
        (r'\\quad', '  '),
        (r'\\\\', '<br>'),
    ]

    for pattern, repl in replacements:
        f = re.sub(pattern, repl, f)

    # Remove remaining backslash commands
    f = re.sub(r'\\[a-zA-Z]+', '', f)
    # Clean up braces
    f = f.replace('{', '').replace('}', '')

    return f


