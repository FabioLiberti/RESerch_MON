def build_keyword_data(keywords: list[str]) -> tuple[list[dict], dict]:
    """Build keyword tags with colors and category grouping."""
    tags = []
    categories: dict[str, dict] = {}

    for kw in keywords:
        cat_name, color, bg = categorize_keyword(kw)
        tags.append({"keyword": kw, "color": color, "bg": bg, "category": cat_name})

        if cat_name not in categories:
            categories[cat_name] = {"color": color, "keywords": []}
        categories[cat_name]["keywords"].append(kw)

    return tags, categories


async def get_paper_data(db: AsyncSession, paper_id: int) -> dict | None:
    """Fetch full paper data for report generation."""
    result = await db.execute(
        select(Paper).where(Paper.id == paper_id)
    )
    paper = result.scalar_one_or_none()
    if not paper:
        return None

    # Get authors
    from app.models.paper import Author, PaperAuthor
    authors_result = await db.execute(
        select(Author)
        .join(PaperAuthor)
        .where(PaperAuthor.paper_id == paper_id)
        .order_by(PaperAuthor.position)
    )
    authors = [a.name for a in authors_result.scalars().all()]

    # Get topics
    topics_result = await db.execute(
        select(Topic)
        .join(PaperTopic)
        .where(PaperTopic.paper_id == paper_id)
    )
    topics = [t.name for t in topics_result.scalars().all()]

    # Get sources
    sources_result = await db.execute(
        select(PaperSource).where(PaperSource.paper_id == paper_id)
    )
    sources = [s.source_name for s in sources_result.scalars().all()]

    return {
        "paper": paper,
        "authors": ", ".join(authors) if authors else None,
        "topics": ", ".join(topics) if topics else None,
        "sources": ", ".join(sources) if sources else None,
    }


def _clean_stray_latex(text: str) -> str:
    """Clean LaTeX remnants in plain text that aren't wrapped in $...$."""
    # Replace \| with | (norm notation)
    text = text.replace('\\|', '|')
    # Replace \hat{x} → x̂, \bar{x} → x̄, \tilde{x} → x̃
    text = re.sub(r'\\hat\{(\w)\}', r'\1̂', text)
    text = re.sub(r'\\bar\{(\w)\}', r'\1̄', text)
    text = re.sub(r'\\tilde\{(\w)\}', r'\1̃', text)
    # Common Greek letters in plain text
    greek = {
        '\\alpha': 'α', '\\beta': 'β', '\\gamma': 'γ', '\\delta': 'δ',
        '\\epsilon': 'ε', '\\theta': 'θ', '\\lambda': 'λ', '\\mu': 'μ',
        '\\sigma': 'σ', '\\pi': 'π', '\\phi': 'φ', '\\omega': 'ω',
        '\\Delta': 'Δ', '\\Sigma': 'Σ', '\\Omega': 'Ω',
    }
    for latex, uni in greek.items():
        text = text.replace(latex, uni)
    # Common operators
    ops = {
        '\\cdot': '·', '\\times': '×', '\\leq': '≤', '\\geq': '≥',
        '\\neq': '≠', '\\approx': '≈', '\\infty': '∞', '\\sum': 'Σ',
        '\\prod': 'Π', '\\int': '∫', '\\partial': '∂', '\\nabla': '∇',
        '\\rightarrow': '→', '\\leftarrow': '←', '\\Rightarrow': '⇒',
        '\\forall': '∀', '\\exists': '∃', '\\in': '∈',
        '\\ell': 'ℓ', '\\quad': ' ', '\\,': '', '\\;': '',
    }
    for latex, uni in ops.items():
        text = text.replace(latex, uni)
    # \frac{a}{b} → (a)/(b) in plain text
    text = re.sub(r'\\frac\{([^}]+)\}\{([^}]+)\}', r'(\1)/(\2)', text)
    # \sqrt{x} → √(x)
    text = re.sub(r'\\sqrt\{([^}]+)\}', r'√(\1)', text)
    # \text{...}, \mathrm{...}, \mathbb{...} → just the content
    text = re.sub(r'\\(?:text|mathrm|mathbb|mathcal)\{([^}]+)\}', r'\1', text)
    # Superscripts and subscripts in plain text
    text = re.sub(r'\^\{([^}]+)\}', r'⁽\1⁾', text)
    text = re.sub(r'_\{([^}]+)\}', r'₍\1₎', text)
    # Remove remaining unknown \commands but keep the text after
    text = re.sub(r'\\[a-zA-Z]+(?:\{([^}]*)\})?', r'\1', text)
    # Clean stray braces
    text = text.replace('{', '').replace('}', '')
    return text


def render_paper_report(paper_data: dict, analysis_text: str, engine: str = "Claude Opus", mode: str = "") -> str:
    """Render the HTML report for a single paper."""
    paper = paper_data["paper"]
    keywords = paper.keywords or []

    keyword_tags, keyword_categories = build_keyword_data(keywords)
    # Strip fenced code blocks that Claude sometimes wraps the output in
    clean_text = analysis_text
    if clean_text.strip().startswith("```"):
        clean_text = re.sub(r'^```\w*\n?', '', clean_text.strip())
        if clean_text.endswith("```"):
            clean_text = clean_text[:-3]
    # Clean stray LaTeX in plain text (not wrapped in $...$)
    clean_text = _clean_stray_latex(clean_text)
    analysis_html = markdown_to_html(clean_text)

    env = Environment(loader=BaseLoader(), autoescape=True)
    template = env.from_string(PAPER_REPORT_TEMPLATE)

    return template.render(
        paper=paper,
        authors=paper_data["authors"],
        topics=paper_data["topics"],
        sources=paper_data["sources"],
        engine=engine,
        mode=mode,
        keyword_tags=keyword_tags,
        keyword_categories=keyword_categories,
        analysis_html=analysis_html,
        generated_at=datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC"),
        pdf_footer_text=_pdf_footer_text_plain(),
    )


def _get_next_version(paper_id: int, mode: str) -> int:
    """Get next version number for a paper+mode combination."""
    reports_dir = Path(settings.reports_path) / "analysis"
    if not reports_dir.exists():
        return 1
    existing = list(reports_dir.glob(f"analysis_{mode}_{paper_id}_v*.html"))
    if not existing:
        return 1
    versions = []
    for p in existing:
        # Extract version from filename: analysis_quick_461_v3.html
        try:
            v = int(p.stem.rsplit("_v", 1)[1])
            versions.append(v)
        except (ValueError, IndexError):
            continue
    return max(versions, default=0) + 1


def save_report(html: str, paper_id: int, mode: str = "quick", version: int | None = None) -> Path:
    """Save HTML report to disk with versioned naming. Returns HTML path."""
    reports_dir = Path(settings.reports_path) / "analysis"
    reports_dir.mkdir(parents=True, exist_ok=True)

    if version is None:
        version = _get_next_version(paper_id, mode)

    basename = f"analysis_{mode}_{paper_id}_v{version}"
    path = reports_dir / f"{basename}.html"
    path.write_text(html, encoding="utf-8")
    logger.info(f"Report saved: {path}")
    return path


def save_markdown(analysis_text: str, paper_id: int, mode: str, paper_data: dict, version: int | None = None) -> Path:
    """Save analysis as Markdown file with versioned naming."""
    reports_dir = Path(settings.reports_path) / "analysis"
