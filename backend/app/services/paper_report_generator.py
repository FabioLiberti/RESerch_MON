def _pdf_footer_text() -> str:
    """Build the LaTeX footer line, including the configurable author signature.

    Reads pdf.author_signature / pdf.author_affiliation from app_settings.
    Falls back to plain 'Generated by FL Research Monitor' if no signature is set.
    """
    from app.services.app_settings import (
        get_setting_sync,
        KEY_PDF_AUTHOR_SIGNATURE,
        KEY_PDF_AUTHOR_AFFILIATION,
    )
    sig = (get_setting_sync(KEY_PDF_AUTHOR_SIGNATURE, "") or "").strip()
    aff = (get_setting_sync(KEY_PDF_AUTHOR_AFFILIATION, "") or "").strip()
    generated_at = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")
    # Escape LaTeX specials in signature/affiliation
    def _e(s: str) -> str:
        return s.replace('&', '\\&').replace('%', '\\%').replace('#', '\\#').replace('_', '\\_').replace('$', '\\$')
    if sig:
        line = _e(sig) + (" --- " + _e(aff) if aff else "")
        return f"Reviewed by {line} \\quad\\textbar\\quad Generated by FL Research Monitor --- {generated_at}"
    return f"Generated by FL Research Monitor --- {generated_at}"


